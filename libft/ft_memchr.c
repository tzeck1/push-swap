#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*p;

	p = (char *)s;
	while (n > 0)
	{
		if (*p == c)
			return (p);
		p++;
		n--;
	}
	return (NULL);
}
