#include "libft.h"

int	ft_isdigit_string(char *input)
{
	int	i;

	i = 0;
	while (input[i] != '\0')
	{
		if (input[i] < 48 || input[i] > 57)
		{
			if (input[i] != '-' && input[i] != '+')
				return (0);
		}
		i++;
	}
	return (1);
}
